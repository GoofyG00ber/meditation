  isRunning.value = false
  if (intervalId) {
    window.clearInterval(intervalId)
    intervalId = undefined
  }
}

function resetTimer() {
  pauseTimer()
  remainingSeconds.value = totalSeconds.value
}

onUnmounted(() => {
  if (intervalId) window.clearInterval(intervalId)
})
