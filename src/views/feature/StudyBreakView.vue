function startTimer() {
  if (remainingSeconds.value === 0) {
    remainingSeconds.value = totalSeconds.value
  }
  
  isRunning.value = true
  intervalId = window.setInterval(() => {
    if (remainingSeconds.value > 0) {
      remainingSeconds.value--
    } else {
      pauseTimer()
    }
  }, 1000)
}

